        <h3>Fördermittel aus dem Förderprogramm Thüringen-Invest erhalten</h3>
        <span></span>
        <img src="informationBilder/Fördermittel.png" alt="" >
        <div class="informationText">
            <p>+++ Tolle Neuigkeiten in eigener Sache +++</p>
            <p>Ende Februar erhielten wir den Zuwendungsbescheid aus dem Förderprogamm "Thüringen-Invest". Wir haben vor dem Umzug des Unternehmens vom Schlossberg 12 zum Arnoldiplatz 5 eine Förderung beantragt und sind sehr glücklich damit, einen Beitrag für die Arbeitsplatzschaffung und -sicherung sowie zur innovativen Arbeitsplatzgestaltung leisten zu können.</p>
            <p>Wir haben beispielsweise unser neues Büro mit ergonomischen Büromöbeln und Akustikelementen ausgestattet und mit einer offenen Büroküche einen zentralen Platz für Kommunkation und Repräsentation geschaffen. Dazu haben wir einen multifunktionalen Konferenzraum eingerichtet. Wir fühlen uns in den neuen Räumlichkeiten sehr wohl und danken dem Freistaat Thüringen für die Unterstützung.</p>
            <p>Das vom Freisstaat Thüringen geförderte Vorhaben wurde durch Mittel der Europäischen Union im Rahmen des Europäischen Fonds für regionale Entwicklung (EFRE) kofinaziert.</p>
